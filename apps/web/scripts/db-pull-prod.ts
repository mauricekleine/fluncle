#!/usr/bin/env bun
/**
 * Dump the PRODUCTION Turso database into the golden local snapshot at
 * apps/web/.dev/seed.sql — the seed that worktrees clone (see db-refresh.ts).
 * Run it (normally in the main checkout) when you want local dev to catch up to
 * production.
 *
 * Production credentials are never stored in .dev.vars. They are read at run
 * time from 1Password (the `Turso Production Credentials` item in the Fluncle
 * vault), so this is a deliberate, human-in-the-loop step: `op` must be
 * unlocked. The dump itself is read-only (SELECTs).
 */
import { $ } from "bun";
import { createClient } from "@libsql/client/web";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";

const ITEM = "op://Fluncle/Turso Production Credentials";

async function readSecret(field: string): Promise<string> {
  try {
    const value = await $`op read ${`${ITEM}/${field}`}`.text();

    return value.trim();
  } catch {
    throw new Error(
      `Could not read ${field} from 1Password (${ITEM}). Unlock 1Password and enable its CLI integration, then retry.`,
    );
  }
}

function quoteIdent(name: string): string {
  return `"${name.replace(/"/g, '""')}"`;
}

function literal(value: unknown): string {
  if (value === null || value === undefined) {
    return "NULL";
  }

  if (typeof value === "number") {
    return Number.isFinite(value) ? String(value) : "NULL";
  }

  if (typeof value === "bigint") {
    return value.toString();
  }

  if (typeof value === "boolean") {
    return value ? "1" : "0";
  }

  if (value instanceof ArrayBuffer || ArrayBuffer.isView(value)) {
    const bytes =
      value instanceof ArrayBuffer
        ? new Uint8Array(value)
        : new Uint8Array((value as ArrayBufferView).buffer);
    let hex = "";

    for (const byte of bytes) {
      hex += byte.toString(16).padStart(2, "0");
    }

    return `X'${hex}'`;
  }

  return `'${(value as string).replace(/'/g, "''")}'`;
}

const url = await readSecret("TURSO_DATABASE_URL");
const authToken = await readSecret("TURSO_AUTH_TOKEN");

const client = createClient({ authToken, url });

const schema = await client.execute(
  `SELECT type, name, sql FROM sqlite_master
   WHERE sql IS NOT NULL
     AND name NOT LIKE 'sqlite_%'
     AND name NOT LIKE 'libsql_%'
     AND name NOT LIKE '_litestream%'
   ORDER BY CASE type WHEN 'table' THEN 0 WHEN 'index' THEN 1 WHEN 'trigger' THEN 2 ELSE 3 END, name`,
);

const tableNames = schema.rows
  .filter((row) => row.type === "table")
  .map((row) => row.name as string);

const parts: string[] = [
  "-- Fluncle production snapshot. Generated by `bun run db:pull-prod`. Do not edit by hand.",
  "PRAGMA foreign_keys=OFF;",
  "BEGIN TRANSACTION;",
];

// Tables first, so the data INSERTs below have something to land in.
for (const row of schema.rows) {
  if (row.type === "table") {
    parts.push(`${row.sql as string};`);
  }
}

// Then the data, table by table.
for (const table of tableNames) {
  const result = await client.execute(`SELECT * FROM ${quoteIdent(table)}`);

  if (result.rows.length === 0) {
    continue;
  }

  const columns = result.columns;
  const columnList = columns.map(quoteIdent).join(", ");

  for (const row of result.rows) {
    const values = columns
      .map((column) => literal((row as Record<string, unknown>)[column]))
      .join(", ");

    parts.push(`INSERT INTO ${quoteIdent(table)} (${columnList}) VALUES (${values});`);
  }
}

// Indexes, triggers and views last — after the rows they depend on exist.
for (const row of schema.rows) {
  if (row.type !== "table") {
    parts.push(`${row.sql as string};`);
  }
}

parts.push("COMMIT;");

const outPath = join(".dev", "seed.sql");

await mkdir(dirname(outPath), { recursive: true });
await writeFile(outPath, `${parts.join("\n")}\n`, "utf8");

console.log(`Wrote ${outPath} (${tableNames.length} tables) from production.`);
